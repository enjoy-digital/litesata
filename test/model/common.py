def _xor_taps(bits, taps):
    value = 0
    for tap in taps:
        value ^= bits[tap]
    return value


def _bits(value, width):
    return [(value >> i) & 0x1 for i in range(width)]


def _bits_to_int(bits):
    value = 0
    for bit in reversed(bits):
        value = (value << 1) | bit
    return value


def sata_crc(datas):
    crc = 0x52325032
    for data in datas:
        crc_bits = _bits(crc ^ data, 32)
        crc = _bits_to_int([_xor_taps(crc_bits, taps) for taps in reversed(crc_taps)])
    return crc


@lru_cache(maxsize=None)
def scrambler_values(length):
    context = 0xf0f6
    values = []
    for _ in range(length):
        now = _bits(context, 16)
        value = _bits_to_int([_xor_taps(now, taps) for taps in reversed(scrambler_taps)])
        context = value >> 16
        values.append(value)
    return tuple(values)
